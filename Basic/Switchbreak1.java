package in.co.rays.Basic;

public class Switchbreak1 {
	public static void main(String []args) {
		int num = 0;
		switch(num) {
		case 10:
		System.out.println("case 1 satisfied");
		break;
		case 20:
			System.out.println("case 2 satisfied");
		break;
		case 30:
			System.out.println("case 3 satisfied");
		break;
		
		default:
			System.out.println("This is default black");
		break;	
		
		
		
		}
	}

}
