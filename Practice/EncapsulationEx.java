package in.co.rays.Practice;

public class EncapsulationEx {
	private int n;
	private int b;
	private int c;
	
	public int getN() {
		return n;
		
	}
	public void setN(int n) {
		this.n = n;



	}
	public int getB() {
		return b;
	}
	public void setB(int b) {
		this.b = b;
	}
	public int getC() {
		return c;
	}
	public void setC(int c) {
		this.c = c;
	}
	
	

}
