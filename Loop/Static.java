package in.co.rays.Loop;

public class Static {
	public static int Sum(int a,int b) {
		if(a>b) {
		return a+b;
		}else {
			return b+a;
		}
	}
	public static int Sub(int a,int b) {
		if(a>b) {
			return a-b;
		}else {
			return b-a;
		}
	}
	public static int mul(int a,int b) {
		return a*b;
	}
	
	

}
